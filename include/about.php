		товары,
		произведенные в Республике Саха (Якутия).</p>
	<p>Местным товаропроизводителям сложно конкурировать с федеральными
		торговыми сетями. Многие предприниматели далеко не всегда имеют сайты, где покупатели могли бы иметь полноценную
		возможность ознакомиться с продуктом и сделать заказ. Онлайн-магазин – это современный инструмент для торговли,
		который позволяет сэкономить время и продавцу и покупателям.</p>
	<p>Где купить товары под брендом «Сделано в Якутии»?
		Конечно же, на Единой торговой площадке! Даже ходить никуда не надо – можно зайти в интернет и за несколько
		кликов
		сделать заказ с доставкой по городу, в районы или в любую точку России, а, возможно, и мира. Многие наши
		производители
		имеют опыт продаж за рубежом.</p>
	<p>Мы хотим, чтобы предпринимательство в Якутии активно развивалось, и спрос на
		местную продукцию был высок. Это и вдохновило нас на создание торгового маркетплейса. Он бесплатный как для
		покупателей,
		так и для продавцов.</p>
	<p>Маркетплейс «Сделано в Якутии» – это проект Министерства предпринимательства,
		торговли и туризма РС (Я), ГАУ «Центр «Мой бизнес» и предпринимательского сообщества республики.</p>
	<p>Здесь производители товаров и услуг:</p>
	<ul>
		<li>презентуют свою продукцию без вложений в создание сайта и рекламу,</li>
		<li>получают онлайн-заказы,</li>
		<li>позиционируют себя как якутский бренд,</li>
		<li>могут продавать товары и услуги покупателям из других регионов.</li>
	</ul>
	<p>Покупатели:</p>
