		<li>выбирают товары по фото и описанию, не выходя из дома,</li>
		<li>могут сравнивать цены,</li>
		<li>прямо на сайте оформляют заказ.</li>
	</ul>
	<p>Нам важно, чтобы товары и услуги, которые продаются с пометкой «Сделано в Якутии», были качественными. Поэтому
